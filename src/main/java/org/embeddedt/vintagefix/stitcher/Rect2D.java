package org.embeddedt.vintagefix.stitcher;

public class Rect2D {
    public int x;
    public int y;
    public int width;
    public int height;

    public Rect2D() { }

    public Rect2D(int x, int y, int width, int height) {
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
    }
}
